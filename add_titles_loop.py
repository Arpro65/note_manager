# Инициализация списка для хранения заголовков
titles = []

# Цикл для запроса заголовков у пользователя
while True:
    title = input("Введите заголовок заметки (или 'стоп' для завершения): ")

    # Проверка на команду завершения
    if title.lower() == "стоп" or title == "":
        break

    # Добавление заголовка в список, если он не пустой
    if title.strip():  # Убедимся, что заголовок не пустой
        titles.append(title)

# Вывод всех добавленных заголовков
print("Список добавленных заголовков:")
for i, title in enumerate(titles, start=1):
    print(f"{i}. {title}")