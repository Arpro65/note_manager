def main():
    # Возможные статусы заметок
    statuses = ['выполнено', 'в процессе', 'отложено']

    # Запрашиваем у пользователя текущий статус заметки
    current_status = input("Введите текущий статус заметки: ")
    print(f"Текущий статус заметки: {current_status}")

    while True:
        # Показываем список возможных статусов
        print("\\nВозможные статусы:")
        for status in statuses:
            print(f"- {status}")

        # Запрашиваем у пользователя новый статус
        new_status = input("Введите новый статус заметки: ")

        # Проверяем корректность введенного значения
        if new_status in statuses:
            current_status = new_status
            print(f"Статус заметки обновлён на: {current_status}")
            break  # Выходим из цикла, так как статус обновлён
        else:
            print("Некорректный статус. Пожалуйста, выберите из предложенных.")

    # Выводим обновлённый статус заметки
    print(f"\\nОбновлённый статус заметки: {current_status}")

if __name__ == "__main__":
    main()